<h1>Tablouri unidimensionale</h1> <br/><br/>
<ol>
<li><b>Definitie.</b> Un tablou este o colectie de date de acelasi tip aranjate contiguu in memorie.</li><br/>
<li><b>Declararea unui tablou unidimensional:<br/><br/>
<tab>tip_baza_nume_tablou[numar_componente];</b> unde:<br/><br/>
<ul><li><b>tip_baza</b>: precizeaza tipul datelor din tablou;</li><br/>
<li><b>nume_tablour</b>: precizeaza numele tabloului;</li><br/>
<li><b>numar_componente</b>: precizeaza cate componente contine tabloul (dimensiunea.</li></ul>
<br/><em>Exemplu:</em><br/><br/>
<tab><code><f>int</f> x[20];</code><br/><br/>
<ul><li>s-a declarat un tablou unidimensional cu numele x;</li><br/>
<li>datele din tablou for fi de tipul <code><f>int</f></code>;</li><br/>
<li>are 20 de componente;</li><br/>
<li>componentele se acceseaza astfel: <code>x[0], x[1], ... , x[19]</code>.</li></ul>
</li><br/>
<li><b>Initializarea tablourilor</b><br/><br/>
Declaratia unui tablou poate fi urmata de o secventa de initializare. Secventa de initializare este formata dintr-o pereche de acolade intre care se pun valori ale tipului de baza separate prin virgula.<br/><br/><em>Exemplu:</em><br/><br/>
<tab><code><f>float</f> y[]={6.1, 7.4e, 8e-2, -3};</code><br/><br/>
<ul><li>s-a declarat un tablou unidimensional cu numele y;</li><br/>
<li>datele din tablou vor fi de tipul <code><f>float</f></code>;</li><br/>
<li>are 4 componente;</li><br/>
<li>dupa aceasta declaratie <code>y[0]=6.1; y[1]=7.4e; y[2]=8e-2; y[3]=-3;</code></li></ul><br/>
</li>

</ol>
