<h1>Matrice varfuri-arce</h1><br/><br/>
Matricea <b>varfuri-arce</b> este o matrice <b>B</b> cu <b>n</b>=|X| linii si <b>m</b>=|U| coloane, in care fiecare element B[i,j] este:
<ul><li>1, daca nodul i este o extremitate initiala a arcului u<sub>j</sub></li>
